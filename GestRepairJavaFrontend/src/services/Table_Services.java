    public void showTable(String[][] list) {
        DefaultTableModel mod = (DefaultTableModel) tbl_schedule.getModel();
        Object[] row = new Object[5];
        for (String[] list1 : list) {
            for (int i = 0; i < row.length; i++) {
                row[i] = list1[i];
            }
            mod.addRow(row);
        }
    }

